package DBSample;

public class UserData {
	private int rid; // RIDを保存
	private String userID; // ユーザ名を保存
	private String passWord; // パスワードを保存

	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String serID) {
		this.userID = serID;
	}
	public String getPassWord() {
		return passWord;
	}
	public void setPassWord(String passWord) {
		this.passWord = passWord;
	}




}
